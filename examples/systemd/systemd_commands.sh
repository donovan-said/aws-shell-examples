#!/bin/bash

########################################################################################################################
########################################### Check if Service is Active #################################################
########################################################################################################################

systemctl is-active --quiet "INSERT_SERVICE_NAME"

########################################################################################################################
########################################### Stop/Start/Reload Service ##################################################
########################################################################################################################

systemctl stop "INSERT_SERVICE_NAME"
systemctl enable "INSERT_SERVICE_NAME" --now

# Used when a SystemD file has been modified
systemctl daemon-reload